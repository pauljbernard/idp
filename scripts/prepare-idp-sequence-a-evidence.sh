      }
    }
  ]
}
EOF

node "${ROOT_DIR}/scripts/render-idp-sequence-a-status-matrix.js" "${MANIFEST_JSON}" > "${STATUS_MATRIX_MD}"
node "${ROOT_DIR}/scripts/update-idp-sequence-a-bundle-status.js" "${MANIFEST_JSON}" >/dev/null
node "${ROOT_DIR}/scripts/render-idp-sequence-a-status-matrix.js" "${MANIFEST_JSON}" > "${STATUS_MATRIX_MD}"
OUTPUT_MODE=markdown node "${ROOT_DIR}/scripts/verify-idp-sequence-a-bundle.js" "${MANIFEST_JSON}" > "${VERIFY_SUMMARY_MD}" || true
OUTPUT_MODE=json node "${ROOT_DIR}/scripts/verify-idp-sequence-a-bundle.js" "${MANIFEST_JSON}" > "${VERIFY_SUMMARY_JSON}" || true

cat <<EOF
sequence_a_output_dir=${OUTPUT_DIR}
sequence_a_readme=${PARENT_README}
sequence_a_manifest_json=${MANIFEST_JSON}
sequence_a_status_matrix_markdown=${STATUS_MATRIX_MD}
sequence_a_verification_markdown=${VERIFY_SUMMARY_MD}
sequence_a_verification_json=${VERIFY_SUMMARY_JSON}
sequence_a_verify_command=npm run verify:idp:sequence-a-bundle -- "${MANIFEST_JSON}"
sequence_a_render_status_matrix_command=npm run render:idp:sequence-a-status-matrix -- "${MANIFEST_JSON}" '>' "${STATUS_MATRIX_MD}"
sequence_a_update_status_command=npm run update:idp:sequence-a-bundle-status -- "${MANIFEST_JSON}"
login_transactions_dir=${OUTPUT_DIR}/login-transactions
tickets_dir=${OUTPUT_DIR}/tickets
sessions_dir=${OUTPUT_DIR}/sessions
issued_tokens_dir=${OUTPUT_DIR}/issued-tokens
generated_at=${TIMESTAMP}
environment_label=${ENV_LABEL}
EOF
