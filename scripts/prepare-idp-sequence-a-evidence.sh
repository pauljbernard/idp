        "preflight_json": "${OUTPUT_DIR}/issued-tokens/issued-tokens-preflight.json",
        "operations_health_json": "${OUTPUT_DIR}/issued-tokens/operations-health.json",
        "preflight_markdown": "${OUTPUT_DIR}/issued-tokens/issued-tokens-preflight.md",
