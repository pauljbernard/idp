      "artifacts": {
        "preflight_json": "${OUTPUT_DIR}/tickets/tickets-preflight.json",
        "operations_health_json": "${OUTPUT_DIR}/tickets/operations-health.json",
        "preflight_markdown": "${OUTPUT_DIR}/tickets/tickets-preflight.md",
        "live_evidence_markdown": "${OUTPUT_DIR}/tickets/tickets-live-evidence.md",
        "readme": "${OUTPUT_DIR}/tickets/README.md"
      }
    },
    {
      "slug": "sessions",
      "step_number": 3,
      "entity_family": "Sessions",
      "output_dir": "${OUTPUT_DIR}/sessions",
      "artifacts": {
        "preflight_json": "${OUTPUT_DIR}/sessions/sessions-preflight.json",
        "operations_health_json": "${OUTPUT_DIR}/sessions/operations-health.json",
        "preflight_markdown": "${OUTPUT_DIR}/sessions/sessions-preflight.md",
        "live_evidence_markdown": "${OUTPUT_DIR}/sessions/sessions-live-evidence.md",
        "readme": "${OUTPUT_DIR}/sessions/README.md"
      }
