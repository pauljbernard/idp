#!/usr/bin/env bash

set -euo pipefail

ROOT_DIR="$(cd "$(dirname "$0")/.." && pwd)"

TIMESTAMP="$(date -u +%Y%m%dT%H%M%SZ)"
ENV_LABEL="${IDP_ENV_LABEL:-default}"
DEFAULT_OUTPUT_DIR="${ROOT_DIR}/tmp/runtime-cutover-evidence/sequence-a-${ENV_LABEL}-${TIMESTAMP}"
OUTPUT_DIR="${1:-${DEFAULT_OUTPUT_DIR}}"
PARENT_README="${OUTPUT_DIR}/README.md"
MANIFEST_JSON="${OUTPUT_DIR}/sequence-a-manifest.json"
STATUS_MATRIX_MD="${OUTPUT_DIR}/sequence-a-status-matrix.md"
VERIFY_SUMMARY_MD="${OUTPUT_DIR}/sequence-a-verification.md"
VERIFY_SUMMARY_JSON="${OUTPUT_DIR}/sequence-a-verification.json"

mkdir -p "${OUTPUT_DIR}"

run_step() {
  local slug="$1"
  local step_number="$2"
  local entity_family="$3"
  local checklist_path="$4"
  local target_dir="${OUTPUT_DIR}/${slug}"

  echo "Preparing Sequence A evidence bundle for ${entity_family}..."
  bash "${ROOT_DIR}/scripts/prepare-idp-runtime-cutover-evidence.sh" \
    "${slug}" \
    "${step_number}" \
    "${entity_family}" \
    "${checklist_path}" \
    "${target_dir}"
}

run_step "login-transactions" "1" "Login transactions" "docs/spec/plans/Headless_IAM_Login_Transaction_Cutover_Checklist.md"
run_step "tickets" "2" "Tickets" "docs/spec/plans/Headless_IAM_Ticket_Cutover_Checklist.md"
run_step "sessions" "3" "Sessions" "docs/spec/plans/Headless_IAM_Session_Cutover_Checklist.md"
run_step "issued-tokens" "4" "Issued tokens" "docs/spec/plans/Headless_IAM_Issued_Token_Cutover_Checklist.md"

cat > "${PARENT_README}" <<EOF
# Sequence A Evidence Bundle

Generated by \`bash scripts/prepare-idp-sequence-a-evidence.sh\`.

Environment inputs:

- \`IDP_BASE_URL\`: target environment base URL used for preflight and auth bootstrap
- \`IDP_ENV_LABEL\`: optional environment label included in the bundle path
- \`IDP_IAM_REALM_ID\`: optional explicit realm override for API-only environments
- \`IDP_IAM_CLIENT_ID\`: optional explicit client override for API-only environments
- \`IDP_ADMIN_EMAIL\`: optional admin email used during preflight authentication
- \`IDP_ADMIN_PASSWORD\`: optional admin password used during preflight authentication

Contained step bundles:

- \`login-transactions/\`: Sequence A / Step 1 bundle
- \`tickets/\`: Sequence A / Step 2 bundle
- \`sessions/\`: Sequence A / Step 3 bundle
- \`issued-tokens/\`: Sequence A / Step 4 bundle

Parent artifact index:

- \`README.md\`: this guide
- \`sequence-a-manifest.json\`: machine-readable bundle inventory
- \`sequence-a-status-matrix.md\`: top-level execution status and Phase 1 go/no-go surface
- \`sequence-a-verification.md\`: latest human-readable completeness check
- \`sequence-a-verification.json\`: latest machine-readable completeness check

Per-step artifact map:

- \`login-transactions/\`
  - \`login-transactions-preflight.json\`
  - \`operations-health.json\`
  - \`login-transactions-preflight.md\`
  - \`login-transactions-live-evidence.md\`
  - \`README.md\`
- \`tickets/\`
  - \`tickets-preflight.json\`
  - \`operations-health.json\`
  - \`tickets-preflight.md\`
  - \`tickets-live-evidence.md\`
  - \`README.md\`
- \`sessions/\`
  - \`sessions-preflight.json\`
  - \`operations-health.json\`
  - \`sessions-preflight.md\`
  - \`sessions-live-evidence.md\`
  - \`README.md\`
- \`issued-tokens/\`
  - \`issued-tokens-preflight.json\`
  - \`operations-health.json\`
  - \`issued-tokens-preflight.md\`
  - \`issued-tokens-live-evidence.md\`
  - \`README.md\`

Recommended operator flow:

1. Confirm each step bundle preflight shows \`runtime_cutover_status=PASS\` and does not report \`NOOP_FALLBACK\`.
2. Execute each checklist in order, recording Stage 0, Stage 1, Stage 2, and rollback evidence in the generated markdown.
3. Attach raw logs or direct storage proof alongside the matching step bundle.
4. Regenerate \`sequence-a-status-matrix.md\` if bundle contents change materially during review.
5. Run the bundle completeness verifier and treat any non-zero exit as a signal that the packet still contains unresolved scaffold placeholders or missing artifacts.
6. Produce a go/no-go summary after all four bundles are complete.

Generated artifacts:

- \`README.md\`: parent bundle guide
- \`sequence-a-manifest.json\`: machine-readable summary of the bundle layout
- \`sequence-a-status-matrix.md\`: formal top-level status and decision document for the execution window
- \`sequence-a-verification.md\`: latest human-readable completeness check result
- \`sequence-a-verification.json\`: latest machine-readable completeness check result

Useful follow-up commands:

- \`npm run render:idp:sequence-a-status-matrix -- "${MANIFEST_JSON}" > "${STATUS_MATRIX_MD}"\`
- \`npm run verify:idp:sequence-a-bundle -- "${MANIFEST_JSON}"\`
- \`OUTPUT_MODE=json npm run verify:idp:sequence-a-bundle -- "${MANIFEST_JSON}"\`
- \`OUTPUT_MODE=markdown npm run verify:idp:sequence-a-bundle -- "${MANIFEST_JSON}" > "${VERIFY_SUMMARY_MD}"\`

Verifier interpretation:

- exit code \`0\`: parent status matrix and all step evidence documents are free of scaffold placeholders and the packet is review-ready
- non-zero exit: at least one expected artifact is missing or still contains unresolved \`<fill>\` placeholders
EOF

cat > "${MANIFEST_JSON}" <<EOF
